function mainRuneIdToName(id){
  switch(id){
    case 8112 : return "Domination/Electrocute/Electrocute"; break;
    case 8124 : return "Domination/Predator/Predator"; break;
    case 8200 : return "7202_Sorcery"; break;
    case 8300 : return "7203_Whimsy"; break;
    case 8400 : return "7204_Resolve"; break;
  }
}
